#!/usr/bin/env python3

PrimeiroNumero = int(input("Give me the first number: "))
SegundoNumero = int(input("Give me the second number: "))

print("%d + %d = %d" % (PrimeiroNumero, SegundoNumero, PrimeiroNumero + SegundoNumero))
print("%d - %d = %d" % (PrimeiroNumero, SegundoNumero, PrimeiroNumero - SegundoNumero))
print("%d / %d = %d" % (PrimeiroNumero, SegundoNumero, PrimeiroNumero / SegundoNumero)) 
print("%d * %d = %d" % (PrimeiroNumero, SegundoNumero, PrimeiroNumero * SegundoNumero))